package Model;

public class Seat {

	int mSeatID;
	int mSeatRow;
	int mSeatNumber;

	public int getmSeatID() {
		return mSeatID;
	}

	public void setmSeatID(int mSeatID) {
		this.mSeatID = mSeatID;
	}

	public int getmSeatRow() {
		return mSeatRow;
	}

	public void setmSeatRow(int mSeatRow) {
		this.mSeatRow = mSeatRow;
	}

	public int getmSeatNumber() {
		return mSeatNumber;
	}

	public void setmSeatNumber(int mSeatNumber) {
		this.mSeatNumber = mSeatNumber;
	}
}
