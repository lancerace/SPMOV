package Model;

public class TheatreMovie {
	private int mTheatreID;
	private int mMovieID;
	private String mTimeSlot;

	public int getmTheatreID() {
		return mTheatreID;
	}

	public void setmTheatreID(int mTheatreID) {
		this.mTheatreID = mTheatreID;
	}

	public int getmMovieID() {
		return mMovieID;
	}

	public void setmMovieID(int mMovieID) {
		this.mMovieID = mMovieID;
	}

	public String getmTimeSlot() {
		return mTimeSlot;
	}

	public void setmTimeSlot(String mTimeSlot) {
		this.mTimeSlot = mTimeSlot;
	}

}
